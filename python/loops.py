#loops
for i in [1,2,3,4,5]:
    print(i)

#range - last number X
print("inside range-------")
for i in range(1,6):
    print(i)

#can be also used in list
names = ["harry", "ron",  "hermione"]
for name in names:
    print(name)

#loop inside string 
name = "Harry"
for character in name:
    print(character)
